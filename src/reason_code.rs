impl ReasonCode
{
    pub fn from_u8(code: u8) -> ReasonCode
    {
        match FromPrimitive::from_u8(code)
        {
            Some(code) => return code,
            None => panic!("couldn't parse reason code because it was malformed!")
        }
    }
}